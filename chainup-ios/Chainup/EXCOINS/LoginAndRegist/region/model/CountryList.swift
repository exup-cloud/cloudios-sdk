    //获取所有国家地区
    class func getAllRegions() -> [RegionEntity]{
        var array : [RegionEntity] = []
        for item in CountryList.countryList{
            let entity = RegionEntity()
            entity.setEntityWithDict(item)
            //黑名单
            if PublicInfoEntity.sharedInstance.limitCountryList.contains(entity.numberCode) == false{
                array.append(entity)
            }
        }
        return array
    }
    
    //根据dialingCode 获取国家地区
    class func getRegion(_ dialingCode : String) -> RegionEntity?{
        for item in CountryList.countryList{
            if let d = item["dialingCode"] as? String , d == dialingCode{
                let entity = RegionEntity()
                entity.setEntityWithDict(item)
                return entity
            }
        }
        return nil
    }
    
    //根据numberCode 获取国家地区
    class func getRegionWithNumber(_ numberCode : String) -> RegionEntity?{
        for item in CountryList.countryList{
            if let n = item["numberCode"] as? String , n == numberCode{
                let entity = RegionEntity()
                entity.setEntityWithDict(item)
                return entity
            }
        }
        return nil
    }
    
